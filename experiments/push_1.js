function main() {
	var a = [];
	var a_a = [[1, 2, 3],
			   [4, 5, 6],
			   [7, 8, 9]];

	var end = a_a.length;
	for (var row = 0; row < end; ++row) {
		a.push(a_a[row][0]);
	}

	print(a);
}

function print(x) {
	console.log(x);
}

////////////////////////////////////////////////////////////////////////////////
// Execute

main();
